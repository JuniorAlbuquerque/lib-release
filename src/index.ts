const sum = (a: number, b: number, c: number) => {
  return a + b + c
}

const subtraction = (a: number, b: number) => {
  return a - b
}

const multiply = (a: number, b: number) => {
  return a * b
}

const divide = (a: number, b: number) => {
  return a / b
}

export {
  sum,
  multiply,
  divide
}